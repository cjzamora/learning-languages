#include <stdio.h>

int main() {
    int x = 10, y = 20;

    printf("Value of x and y is: %d and %d\n", x, y);
    // == operator
    printf("Equal operator: x == y is: %d\n", x == y);
    // != operator
    printf("Not equal operator: x != y is: %d\n", x != y);
    // > operator
    printf("Greater than operator: x > y is: %d\n", x > y);
    // < operator
    printf("Less than operator: x < y is: %d\n", x < y);
    // >= operator
    printf("Greater than or equal operator: x >= y is: %d\n", x >= y);
    // <= operator
    printf("Less than or equal operator: x <= y is: %d\n", x <= y);

    return 0;
}